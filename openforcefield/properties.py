from enum import Enum, unique


# =============================================================================================
# PropertyType
# =============================================================================================

@unique
class PropertyType(Enum):

    Undefined = 0
    MassDensity = 1

    def __str__(self):
        return self.name

    def __repr__(self):
        return str(self)


# =============================================================================================
# PropertyPhase
# =============================================================================================

@unique
class PropertyPhase(Enum):

    Undefined = 0
    Solid = 1
    Liquid = 2
    Gas = 4

    def __str__(self):
        return self.name

    def __repr__(self):
        return str(self)
